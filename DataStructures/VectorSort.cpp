// C++ program to implement a sort in vectors
//Problem statement link: https://www.hackerrank.com/challenges/vector-sort/problem

#include <cmath>
#include <cstdio>
#include <vector>
#include <iostream>
#include <algorithm>
using namespace std;


int main() {
    /* Enter your code here. Read input from STDIN. Print output to STDOUT */  
    vector<int> v;
    int k;
    cin>> k;
    for(int j=0;j<k;j++){
        int num;
        cin>>num;
        v.push_back(num);
    }
    sort(v.begin(),v.end());
    int s = v.size();
    for(int i=0;i<s;i++){
        cout<<v[i]<<" ";
    }
    return 0;
}
